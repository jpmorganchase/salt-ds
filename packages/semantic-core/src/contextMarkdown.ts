import type { SaltContextComponent } from "./contextArtifacts.js";
import type {
  SaltGeneratedArtifactGenerator,
  SaltGeneratedArtifactRegistry,
} from "./evidence.js";

export const SALT_CONTEXT_COMPONENT_MARKDOWN_CONTRACT =
  "salt_context_component_markdown_v1" as const;

export interface SaltContextComponentMarkdownBridge {
  contract: typeof SALT_CONTEXT_COMPONENT_MARKDOWN_CONTRACT;
  generated_at: string;
  generator: SaltGeneratedArtifactGenerator;
  registry: SaltGeneratedArtifactRegistry;
  status: SaltContextComponent["status"];
  component_id: string;
  component_name: string;
  source_context_contract: SaltContextComponent["contract"];
  source_generated_artifact_id: string;
  generated_artifact_id: string;
  generated_artifact_kind: "component-markdown-bridge";
  evidence_ref_ids: string[];
  evidence_ref_count: number;
  unsupported_claim_count: number;
  missing: string[];
  text: string;
}

export interface SaltContextComponentMarkdownCheckResult {
  current: boolean;
  supported: boolean;
  mismatches: string[];
  missing: string[];
}

function uniqueStrings(values: string[]): string[] {
  return [...new Set(values.map((value) => value.trim()))].filter(
    (value) => value.length > 0,
  );
}

function evidenceMarker(evidenceRefIds: string[]): string {
  return `[EvidenceRef: ${evidenceRefIds.join(", ")}]`;
}

function evidenceText(value: {
  value: string;
  evidence_ref_ids: string[];
}): string {
  return `${value.value} ${evidenceMarker(value.evidence_ref_ids)}`;
}

function normalizeGeneratedAt(text: string): string {
  return text.replace(
    /^<!-- Generated At: .+ -->$/m,
    "<!-- Generated At: <ignored> -->",
  );
}

export function normalizeComponentContextMarkdownForCheck(
  text: string,
): string {
  return normalizeGeneratedAt(text).trim();
}

export function buildComponentContextMarkdownBridge(
  context: SaltContextComponent,
): SaltContextComponentMarkdownBridge {
  const evidenceRefIds = uniqueStrings([
    ...context.component.name.evidence_ref_ids,
    ...context.component.package_name.evidence_ref_ids,
    ...context.component.status.evidence_ref_ids,
    ...(context.component.import?.evidence_ref_ids ?? []),
    ...(context.component.summary?.evidence_ref_ids ?? []),
    ...context.component.props.flatMap((prop) => prop.evidence_ref_ids),
    ...context.component.accessibility.summary.flatMap(
      (entry) => entry.evidence_ref_ids,
    ),
    ...context.component.examples.flatMap(
      (example) => example.evidence_ref_ids,
    ),
  ]);
  const lines = [
    "<!-- Generated by salt-ds export-context. Do not edit directly unless you accept local divergence. -->",
    `<!-- Contract: ${SALT_CONTEXT_COMPONENT_MARKDOWN_CONTRACT} -->`,
    `<!-- Source Context: ${context.contract} ${context.generated_artifact.id} -->`,
    `<!-- Registry: version ${context.registry.version ?? "unknown"}, hash ${context.registry.hash ?? "unknown"}, generated ${context.registry.generated_at ?? "unknown"} -->`,
    `<!-- Generated At: ${context.generated_at} -->`,
    "",
    `# ${evidenceText(context.component.name)}`,
    "",
    `- Package: ${evidenceText(context.component.package_name)}`,
    `- Status: ${evidenceText(context.component.status)}`,
    ...(context.component.import
      ? [
          `- Import: \`${context.component.import.statement}\` ${evidenceMarker(
            context.component.import.evidence_ref_ids,
          )}`,
        ]
      : []),
    ...(context.component.summary
      ? ["", "## Summary", "", evidenceText(context.component.summary)]
      : []),
    ...(context.component.props.length > 0
      ? [
          "",
          "## Props",
          "",
          ...context.component.props.map(
            (prop) =>
              `- \`${prop.name}\` (${prop.required ? "required" : "optional"}, \`${prop.type}\`): ${prop.description} ${evidenceMarker(prop.evidence_ref_ids)}`,
          ),
        ]
      : []),
    ...(context.component.accessibility.summary.length > 0
      ? [
          "",
          "## Accessibility",
          "",
          ...context.component.accessibility.summary.map(
            (entry) => `- ${evidenceText(entry)}`,
          ),
        ]
      : []),
    ...(context.component.examples.length > 0
      ? [
          "",
          "## Examples",
          "",
          ...context.component.examples.flatMap((example) => [
            `- ${example.title}: ${example.description} ${evidenceMarker(
              example.evidence_ref_ids,
            )}`,
            `  Source: ${example.source_url} ${evidenceMarker(
              example.evidence_ref_ids,
            )}`,
            "",
            "```tsx",
            example.code,
            "```",
          ]),
        ]
      : []),
    ...(context.unsupported_claims.length > 0
      ? [
          "",
          "## Unsupported Claims",
          "",
          ...context.unsupported_claims.map(
            (claim) => `- ${claim.field_path ?? claim.id}: ${claim.reason}`,
          ),
        ]
      : []),
    "",
  ];
  const text = lines.join("\n");

  return {
    contract: SALT_CONTEXT_COMPONENT_MARKDOWN_CONTRACT,
    generated_at: context.generated_at,
    generator: context.generator,
    registry: context.registry,
    status: context.status,
    component_id: context.component.id,
    component_name: context.component.name.value,
    source_context_contract: context.contract,
    source_generated_artifact_id: context.generated_artifact.id,
    generated_artifact_id: `component-markdown-bridge.${context.component.id}`,
    generated_artifact_kind: "component-markdown-bridge",
    evidence_ref_ids: evidenceRefIds,
    evidence_ref_count: evidenceRefIds.length,
    unsupported_claim_count: context.unsupported_claims.length,
    missing: context.surface_gate.missing,
    text,
  };
}

export function checkComponentContextMarkdownBridge(input: {
  bridge: SaltContextComponentMarkdownBridge;
  existing_text: string;
}): SaltContextComponentMarkdownCheckResult {
  const current =
    normalizeComponentContextMarkdownForCheck(input.bridge.text) ===
    normalizeComponentContextMarkdownForCheck(input.existing_text);
  const supported = input.bridge.status === "validated";

  return {
    current: current && supported,
    supported,
    mismatches: current ? [] : ["markdown"],
    missing: input.bridge.missing,
  };
}
